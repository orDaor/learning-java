public class TypeCasting {
    public static void main(String[] args) {

        //NOTE: type casting = type conversion

        System.out.println("CONVERT (CAST) double TO int");

        double a = 3.33;
        int b = (int)a; //type casting/conversion of a value
        int c = (int)5860.777;
        System.out.println(b);
        System.out.println(c);
    }
}
